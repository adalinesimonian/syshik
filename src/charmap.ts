function createRegexPatternMap<T extends Record<string, string[]>>(
  obj: T
): Record<keyof T, string> & Record<`${Uppercase<Extract<keyof T, string>>}`, string> {
  const result: Record<string, string> = {}
  for (const key of Object.keys(obj)) {
    const patterns = obj[key]
    result[key] =
      patterns.length === 1
        ? escapeForRegex(patterns[0])
        : `(?:${patterns.map(escapeForRegex).join('|')})`
    result[key.toUpperCase()] = result[key]
  }
  return result as Record<keyof T, string>
}

